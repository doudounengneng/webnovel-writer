export class MockAdapter extends BaseLLMAdapter {
  private responses: Record<string, string>;

  constructor(config: LLMConfig) {
    super(config);
    this.responses = { ...DEFAULT_MOCK_RESPONSES, ...config.mockResponses };
  }

  async chat(messages: LLMMessage[]): Promise<LLMResponse> {
    const lastUserMsg = messages.filter(m => m.role === 'user').pop();
    const content = lastUserMsg?.content || '';

    // Match by stage keyword in the user message
    for (const [key, response] of Object.entries(this.responses)) {
      if (content.includes(key)) {
        return { content: response };
      }
    }

    // Fallback: return the first available mock response
    const fallback = Object.values(this.responses)[0] || 'No mock response configured.';
    return { content: fallback };
  }

  async *chatStream(messages: LLMMessage[]): AsyncGenerator<string, void, void> {
    const response = await this.chat(messages);
    // Simulate streaming by yielding word by word
    const words = response.content.split(/(\s+)/);
    for (const word of words) {
      yield word;
      await new Promise(r => setTimeout(r, 10));
    }
  }

  async listModels(): Promise<string[]> {
    return ['mock-v1'];
  }
}